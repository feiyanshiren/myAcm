import java.io.*;
import java.util.*;
public class Main{
    public static void main(String args[]) throws Exception{
        Scanner cin=new Scanner(System.in);
        int a=cin.nextInt();
        for (int i=0;i<a;i++) {
            int b=cin.nextInt();
            System.out.print(b+"\n");
        }
    }
}